package com.deya.wcm.bean.appeal.count;

public class HandleBean {
	private String handle_id = "";
	private String handle_name = "";
	private Boolean handle_selected = false;
	public String getHandle_id() {
		return handle_id;
	}
	public void setHandle_id(String handle_id) {
		this.handle_id = handle_id;
	}
	public String getHandle_name() {
		return handle_name;
	}
	public void setHandle_name(String handle_name) {
		this.handle_name = handle_name;
	}
	public Boolean getHandle_selected() {
		return handle_selected;
	}
	public void setHandle_selected(Boolean handle_selected) {
		this.handle_selected = handle_selected;
	}
}
